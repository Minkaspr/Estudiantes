
package entity;

public class EstudPasat {
    private Integer idEstudPasat;
    private Integer estudiante;
    private Integer pasatiempo;

    public EstudPasat() {
    }

    public Integer getIdEstudPasat() {
        return idEstudPasat;
    }

    public void setIdEstudPasat(Integer idEstudPasat) {
        this.idEstudPasat = idEstudPasat;
    }

    public Integer getEstudiante() {
        return estudiante;
    }

    public void setEstudiante(Integer estudiante) {
        this.estudiante = estudiante;
    }

    public Integer getPasatiempo() {
        return pasatiempo;
    }

    public void setPasatiempo(Integer pasatiempo) {
        this.pasatiempo = pasatiempo;
    }
}
